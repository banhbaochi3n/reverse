int TwoCharsToInt(unsigned char char1, unsigned char char2)
{
	int number = (char1 << 8) | char2; // Kết hợp 2 ký tự thành 1 số nguyên
	return number;
}

int FindCharPosition(const char *str, char target)
{
	int position = -1;
	for (int i = 0; str[i] != '\0'; ++i)
	{
		if (str[i] == target)
		{
			position = i;
			break;
		}
	}
	return position;
}

